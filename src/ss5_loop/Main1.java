package ss5_loop;

public class Main1 {
    public static void main(String[] args) {

        for (int i = 1; i <= 4; i++) {
            System.out.print(i + "\t");
        }

        //i = 1; 1 < 4 => true => sout 1
        //i = 2; 2 < 4 => true => sout 2
        //i = 3; 3 < 4 => true => sout 3
        //i = 4; 4 < 4 => false => thoát vòng lặp
    }
}
